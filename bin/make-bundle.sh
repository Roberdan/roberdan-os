#!/usr/bin/env bash
# make-bundle.sh — concatenates ONLY the committed canon into 1 pasteable doc for
# ChatGPT / Claude web (Custom Instructions / Project). ALWAYS EXCLUDES private/.
#
# Security (the only artifact that leaves the machine): writes to tmp, runs an
# EXPLICIT leak-check on the tmp, and promotes the output only if the check passes
# (exit 0). A failed check never leaves a leaky file on disk.
#
#   bin/make-bundle.sh            → platforms/chatgpt/bundle.md
#   bin/make-bundle.sh -          → stdout (always after leak-check)
set -euo pipefail

ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
cd "$ROOT"

OUT="${1:-$ROOT/platforms/chatgpt/bundle.md}"
TMP="$(mktemp "${TMPDIR:-/tmp}/rda-bundle.XXXXXX.md")"
trap 'rm -f "$TMP"' EXIT

# The ONLY sources allowed into the bundle — all committed canon, never private/.
SECTIONS=(
  "behavior/roberto-mode.md"
  "identity/voice.md"
  "behavior/thinking-toolkit.md"
  "rules/constitution.md"
  "rules/best-practices.md"
)

{
  echo "# roberdan-os — canonical bundle (pasteable)"
  echo
  echo "> Generated by bin/make-bundle.sh from the committed canon. Does NOT contain the"
  echo "> confidential dossier (private/). Paste into Custom Instructions / Project."
  echo
  for f in "${SECTIONS[@]}"; do
    [ -f "$f" ] || { echo "make-bundle: missing $f" >&2; exit 1; }
    echo "---"; echo
    cat "$f"; echo
  done
  # Agent index (name + description from frontmatter only — not the whole file).
  echo "---"; echo
  echo "## Agents (index)"; echo
  for a in $(find agents -maxdepth 1 -name '*.md' | LC_ALL=C sort); do
    n="$(grep -m1 -E '^name:' "$a" | sed -E 's/^name:[[:space:]]*//')"
    d="$(grep -m1 -E '^description:' "$a" | sed -E 's/^description:[[:space:]]*//')"
    echo "- **$n** — $d"
  done
} > "$TMP"

# GATE: EXPLICIT leak-check on the tmp (the bundle lives outside git: the git-walk misses it).
if ! "$ROOT/test/leak-check.sh" "$TMP" >/dev/null 2>&1; then
  echo "make-bundle: LEAK in the bundle — output NOT promoted. Run test/leak-check.sh \"$TMP\" for details." >&2
  exit 1
fi

if [ "$OUT" = "-" ]; then
  cat "$TMP"
else
  mkdir -p "$(dirname "$OUT")"
  mv "$TMP" "$OUT"
  trap - EXIT
  echo "make-bundle: clean bundle at $OUT ($(wc -l < "$OUT") lines)."
fi

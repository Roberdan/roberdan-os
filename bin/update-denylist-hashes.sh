#!/usr/bin/env bash
# update-denylist-hashes.sh — turns the local-only private/.denylist into a salted-hash
# fingerprint (test/denylist.sha256) that CAN be committed and checked in CI, without ever
# putting the confidential terms themselves in git.
#
# Roberto-only: only runs where private/.denylist exists (never in CI, never on a machine
# without the dossier). Run it locally after editing private/.denylist, then commit the
# regenerated test/denylist.sha256 — that's what activates tier (b) of test/leak-check.sh
# in CI (see that script's header for the 3-tier fallback).
#
# What it does:
#   1. Reads the active lines of private/.denylist (skips comments/blank lines, same rule
#      as leak-check.sh).
#   2. Normalizes each entry: lowercase, all whitespace runs (space/tab/newline/CR)
#      collapsed to a single space, leading/trailing space trimmed. NOTE: this is a literal
#      normalization, not a regex engine — if an entry relies on regex features (\b,
#      alternation, anchors, ...) for matching in tier (a), the hash tier only matches the
#      literal characters, which is a narrower (more false-negative-prone) check. Prefer
#      literal phrases in the denylist if you want both tiers to behave the same.
#   3. Computes a salted SHA-256 per normalized entry: sha256(salt + "\n" + entry). The salt
#      is regenerated (rotated) every run — 32 random bytes, hex-encoded.
#   4. Writes test/denylist.sha256: a header documenting the format/salt/max-words/
#      regeneration steps, followed by the sorted, de-duplicated list of hex hashes.
#
# Honest tradeoff (see also AGENTS.md § Privacy and test/leak-check.sh's header): a committed
# salt stops casual reading of the denylist in the repo and keeps CI logs free of confidential
# terms, but it does NOT stop a determined dictionary attack — anyone with the salt can hash a
# guessed name/phrase and compare against the committed hash list. This upgrades CI from
# "cannot check at all" (no private/ present) to "can check without revealing the list" — it is
# not cryptographic secrecy for the names themselves.
set -euo pipefail

ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
# Overridable for tests (see test/test-leak-check.sh) — unset in normal use.
DENYLIST="${RDA_DENYLIST_SRC:-$ROOT/private/.denylist}"
OUT="${RDA_DENYLIST_OUT:-$ROOT/test/denylist.sha256}"

if [[ ! -f "$DENYLIST" ]]; then
  echo "update-denylist-hashes: REFUSED — $DENYLIST not found." >&2
  echo "  This script only runs on a machine with the local-only dossier (private/)." >&2
  echo "  Nothing to do here; test/leak-check.sh falls back to its no-op warning tier." >&2
  exit 1
fi

sha256_hex() {
  # Portable sha256 of stdin -> lowercase hex, no filename suffix.
  if command -v sha256sum >/dev/null 2>&1; then
    sha256sum | cut -d' ' -f1
  elif command -v shasum >/dev/null 2>&1; then
    shasum -a 256 | cut -d' ' -f1
  else
    echo "update-denylist-hashes: FATAL — no sha256sum/shasum found" >&2
    exit 1
  fi
}

gen_salt() {
  if command -v openssl >/dev/null 2>&1; then
    openssl rand -hex 32
  else
    od -An -tx1 -N32 /dev/urandom | tr -d ' \n'
  fi
}

normalize() {
  # lowercase, collapse all whitespace to single spaces, trim ends. Must match the
  # normalization used by test/leak-check.sh's tier (b) scanner exactly, or hashes never match.
  printf '%s' "$1" \
    | tr '[:upper:]' '[:lower:]' \
    | tr '\t\n\r' '   ' \
    | sed -E 's/  +/ /g; s/^ +//; s/ +$//'
}

patterns="$(grep -vE '^\s*(#|$)' "$DENYLIST" || true)"
if [[ -z "$patterns" ]]; then
  echo "update-denylist-hashes: denylist is empty, nothing to hash." >&2
  exit 1
fi

salt="$(gen_salt)"
max_words=1
hashes_tmp="$(mktemp "${TMPDIR:-/tmp}/rda-denylist-hashes.XXXXXX")"
trap 'rm -f "$hashes_tmp"' EXIT

while IFS= read -r raw; do
  [[ -z "$raw" ]] && continue
  norm="$(normalize "$raw")"
  [[ -z "$norm" ]] && continue
  words=$(( $(printf '%s' "$norm" | tr ' ' '\n' | grep -c . || true) ))
  [[ "$words" -gt "$max_words" ]] && max_words="$words"
  printf '%s\n%s' "$salt" "$norm" | sha256_hex >> "$hashes_tmp"
done <<< "$patterns"

{
  echo "# roberdan-os salted-leak-hash denylist — generated by bin/update-denylist-hashes.sh"
  echo "# DO NOT hand-edit. Regenerate: bin/update-denylist-hashes.sh (rotates the salt), then"
  echo "# commit this file so CI's tier (b) check (test/leak-check.sh) can activate."
  echo "#"
  echo "# Format: one hex SHA-256 per line = sha256(salt + \"\\n\" + normalized-entry-or-ngram)."
  echo "# normalized = lowercase, whitespace runs collapsed to a single space, trimmed."
  echo "#"
  echo "# salt: $salt"
  echo "# max-words: $max_words"
  echo "#"
  echo "# Honest tradeoff: a committed salt stops casual reading of the denylist and keeps CI"
  echo "# logs free of confidential terms, but does NOT stop a dictionary attack against"
  echo "# guessed names (anyone can hash a guess with this salt and compare). This upgrades CI"
  echo "# from \"cannot check\" to \"checks without revealing the list\", not to secrecy of the"
  echo "# names themselves. See AGENTS.md § Privacy."
  LC_ALL=C sort -u "$hashes_tmp"
} > "$OUT"

n="$(LC_ALL=C sort -u "$hashes_tmp" | wc -l | tr -d ' ')"
echo "update-denylist-hashes: wrote $n hash(es), max-words=$max_words, salt rotated -> $OUT"
echo "Commit test/denylist.sha256 to activate CI's hash-based leak check."
